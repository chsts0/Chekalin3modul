function changeOpacity() {
	var soup_1st = document.getElementById("soup_1st");
	var soup_2nd = document.getElementById("soup_2nd");
	var name_1st_soup = document.getElementById("name_1st_soup");
	var name_2nd_soup = document.getElementById("name_2nd_soup");

	soup_1st.style.opacity = "0";
	name_1st_soup.style.opacity = "0";
	soup_2nd.style.opacity = "1";
	name_2nd_soup.style.opacity = "1";
}

